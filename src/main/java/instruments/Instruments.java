package instruments;

public class Instruments {

    String make;
    String colour;
    String material;
    int purchasePrice;
    int sellingPrice;

    public Instruments() {
        this.make = make;
        this.colour = colour;
        this.material = material;
    }

    public String getMake() {
        return make;
    }

    public String getColour() {
        return colour;
    }

    public String getMaterial() {
        return material;
    }

}
